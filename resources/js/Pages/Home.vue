<script setup>

import { ref, onMounted } from 'vue';
import { useRouter } from 'vue-router';
import { useUserStore } from '@/stores/user.js'
import { useProductStore } from '@/stores/product.js'

import UserHeader from './inc/Header.vue';
// import Navbar from './inc/Navbar.vue'
import UserFooter from './inc/Footer.vue';

const router = useRouter()
const store = useUserStore()
const productStore = useProductStore()

let products = ref([])

let paidproducts = ref([])

let unpaidproducts = ref([])

const form = ref({
    firstname: '',
    lastname: '',
    email: '',
    password: '',
    password_confirmation: ''
})

const imageLink = (name) => {
    return "/storage/" + name
}

let login = async () => {
    let response = await axios.post('/login', form.value);
    if (response.status === 200) {
        const { token, user_type, user } = response.data;
        localStorage.setItem('token', token);
        store.authuser = user
        store.token = token
        if (store.token) {
            // router.push({ name: 'account' });
            window.location.href = '/account'
        } else {
            router.push('/');
            window.location.href = '/'
        }
    }
};


let register = async () => {
    let response = await axios.post('/register', form.value);
    if (response.status === 200) {
        const { token, user_type, user } = response.data;
        localStorage.setItem('token', token);
        store.authuser = user
        store.token = token
        if (store.token) {
            // router.push({ name: 'account' });
            window.location.href = '/account'
        } else {
            // router.push('/');
            window.location.href = '/'
        }
    }

};

const getAllProducts = async () => {
    try {
        const response = await axios.get('/get-products');
        products.value = response.data.products;
        productStore.products = response.data.products;
    } catch (error) {
        console.error(error);
    }
};

const getAllPaidProducts = async () => {
    try {
        const response = await axios.get('/get-paid-products');
        paidproducts.value = response.data.paidproducts;
        productStore.paidproducts = response.data.paidproducts;
    } catch (error) {
        console.error(error);
    }
};

const getAllUnpaidProducts = async () => {
    try {
        const response = await axios.get('/get-unpaid-products');
        unpaidproducts.value = response.data.unpaidproducts;
        productStore.unpaidproducts = response.data.unpaidproducts;
    } catch (error) {
        console.error(error);
    }
};

onMounted(() => {
    getAllProducts()
    getAllPaidProducts()
    getAllUnpaidProducts()
});

